package PatternProgramming;

public class Pattern10 {

	public static void main(String[] args) {
		
		
		for( int i = 0; i< 3; i++)
		{
			
			char ch = 'a';
			for( int j = 0; j<6; j++)
			{
				
				System.out.print(ch  );
				ch++;
				
				
			}System.out.println();
		}
		

	}

}
