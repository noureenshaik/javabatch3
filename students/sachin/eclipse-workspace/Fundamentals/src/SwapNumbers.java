
public class SwapNumbers {
	public static void main (String[] args) {
		int fno = 100;
		int sno = 200;
		int temp = 0;
		
		System.out.println("Before swapping: ");
		System.out.println("Fno : " + fno + "\t"+ "Sno : "+ sno);
		
		//swapping
		temp = fno;
		fno = sno;
		sno = temp;
		
		System.out.println("After Swapping: ");
		System.out.println("Fno: "+fno + "\t" + "Sno: " + sno);
		
	}

}
